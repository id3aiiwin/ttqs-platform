async function updateContent(standardName: string, content: string) {
  const res = await fetch(
    `${SUPABASE_URL}/rest/v1/knowledge_base_templates?standard_name=eq.${encodeURIComponent(standardName)}&is_system=eq.true`,
    { method: 'PATCH', headers, body: JSON.stringify({ content }) }
  )
  if (!res.ok) {
    console.error(`Failed: ${standardName}`, await res.text())
  } else {
    console.log(`Updated: ${standardName}`)
  }
}

async function main() {
  for (const [name, content] of Object.entries(CONTENT_MAP)) {
    await updateContent(name, content)
  }
  console.log('\nDone! Updated', Object.keys(CONTENT_MAP).length, 'templates')
}

