/** 按構面分組 */
export function getIndicatorsByPhase() {
  const grouped: Record<string, IndicatorGuide[]> = { P: [], D: [], DO: [], R: [], O: [] }
  ALL_INDICATORS.forEach((ind) => {
    grouped[ind.phase].push(ind)
  })
  return grouped
}

